package com.shanghai.vo;

public class Welcome {
    private Integer musicNumber;

    private Integer singerNumber;

    private Integer albumNumber;

    private Integer listNumber;

    public Integer getMusicNumber() {
        return musicNumber;
    }

    public void setMusicNumber(Integer musicNumber) {
        this.musicNumber = musicNumber;
    }

    public Integer getSingerNumber() {
        return singerNumber;
    }

    public void setSingerNumber(Integer singerNumber) {
        this.singerNumber = singerNumber;
    }

    public Integer getAlbumNumber() {
        return albumNumber;
    }

    public void setAlbumNumber(Integer albumNumber) {
        this.albumNumber = albumNumber;
    }

    public Integer getListNumber() {
        return listNumber;
    }

    public void setListNumber(Integer listNumber) {
        this.listNumber = listNumber;
    }
}
